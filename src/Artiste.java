public class Artiste {

    private int identifiant;
    private String nom;
    private String prenom;
    private String genre;

    public Artiste(int identifiant, String nom, String prenom, String genre) {

        this.identifiant = identifiant;
        this.nom = nom;
        this.prenom = prenom;
        this.genre = genre;

    }

}
